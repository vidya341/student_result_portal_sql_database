    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");//from jdbc driver class we can connect to DB
            //connect to DB USING driver manager from class
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Vidya@0804shivakumar");
            //create statement since sql works on statements only
            Statement st = con.createStatement();
            //in java.sql.* - we have ResultSet to store record of queried student
            String rollno = Rollno.getText(); 
            ResultSet res = st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"' ");
            if(res.next())//at 0th row it vl be empty the res starts from index 1 check if we have record 
            {
                //if yes we have
                //update result
                courseset.setText(res.getString(2));
                branchset.setText(res.getString(3));
                courseset.setText(res.getString(2));
                nameset.setText(res.getString(4));
                genderset.setText(res.getString(5));
                fatherset.setText(res.getString(6));
                dsa1set.setText(res.getString(8));
                dsa2set.setText(res.getString(9));
                dsa3set.setText(res.getString(10));
                dsa4set.setText(res.getString(11));
                float m1 = Float.parseFloat(dsa1set.getText());
                float m2 = Float.parseFloat(dsa2set.getText());
                float m3 = Float.parseFloat(dsa3set.getText());
                float m4 = Float.parseFloat(dsa4set.getText());
                float total = m1+m2+m3+m4;
                String totalMarks = total+"";
                totalset.setText(totalMarks);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No details found since this Roll no is not Registered!");
            }
           
            
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

