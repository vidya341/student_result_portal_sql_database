    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String Rollno = (String) rollno.getText();
        String DSA1 = (String) dsa1.getText();
        String DSA2 = (String) dsa2.getText();
        String DSA3 = (String) dsa3.getText();
        String DSA4 = (String) dsa4.getText();
        try
        {
            Class.forName("com.mysql.cj.jdbc.Driver");//from jdbc driver class we can connect to DB
            //connect to DB USING driver manager from class
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Vidya@0804shivakumar");
            //create statement since sql works on statements only
            Statement st = con.createStatement();
            //in java.sql.* - we have ResultSet to store record of queried student
            ResultSet res = st.executeQuery("select * from student where rollno='"+Rollno+"' ");
            if(res.next())//at 0th row it vl be empty the res starts from index 1 check if we have record 
            {
                //if yes we have
                //update result
                st.executeUpdate("insert into result(rollno,DSA1,DSA2,DSA3,DSA4) values('"+Rollno+"','"+DSA1+"','"+DSA2+"','"+DSA3+"','"+DSA4+"')");
                JOptionPane.showMessageDialog(null,"Database updated with Results");
                setVisible(false);
                new result().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Roll no is not Registered!");
            }
           
            
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_saveActionPerformed

